package exemplosBasicos;

public class TipoPrimitivo {

	public static void main(String[] args) {
		double precoProduto = 9.43;

		System.out.println("Preço do produto: " + precoProduto + "\n");

		boolean alunoMatriculado = true;
		boolean clienteBloqueado = false;

		System.out.println("Aluno matriculado: " + alunoMatriculado);
		System.out.println("Cliente bloqueado: " + clienteBloqueado+ "\n");

		char turmaAluno = 'A';
		char tipoCliente = '2';
		char simbolo = '@';

		System.out.println("Turma: " + turmaAluno);
		System.out.println("Tipo Cliente: " + tipoCliente);
		System.out.println("Simbolo: " + simbolo + "\n");

		long populacaoCidade = 650000;
		System.out.println("População da cidade: " + populacaoCidade + "\n");

		long populacaoMundial = 7000000000L;
		System.out.println("Populacao Mundial: " + populacaoMundial + "\n");

		float saldoConta = 1030.43F;
		System.out.println("Saldo da conta: " + saldoConta);

	}

}
